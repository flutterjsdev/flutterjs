/**
 * AxisDirection - Direction along an axis
 */
const AxisDirection = Object.freeze({
  // Positive X direction (left to right)
  right: 'right',

  // Negative X direction (right to left)
  left: 'left',

  // Positive Y direction (top to bottom)
  down: 'down',

  // Negative Y direction (bottom to top)
  up: 'up'
});
export { AxisDirection };