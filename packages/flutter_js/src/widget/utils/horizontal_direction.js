/**
 * HorizontalDirection - Direction of horizontal axis
 * Controls the order in which children are laid out horizontally
 */
const HorizontalDirection = Object.freeze({
  // Children are laid out from left to right (default, LTR)
  ltr: 'ltr',

  // Children are laid out from right to left (RTL)
  rtl: 'rtl'
});
export { HorizontalDirection };