/**
 * VerticalDirection - Direction of vertical axis
 * Controls the order in which children are laid out vertically
 */
const VerticalDirection = Object.freeze({
  // Children are laid out from top to bottom (default)
  down: 'down',

  // Children are laid out from bottom to top
  up: 'up'
});
export { VerticalDirection };